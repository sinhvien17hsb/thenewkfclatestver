export const getMenuByCategory = (category: string) =>
  menuItems.filter((item) => item.category === category && item.available);

export const getPopularItems = () =>
  menuItems.filter((item) => item.popular && item.available).slice(0, 8);

export const searchMenuItems = (query: string) => {
  const q = query.toLowerCase();
  return menuItems.filter(
    (item) =>
      item.name.toLowerCase().includes(q) ||
      item.nameEn.toLowerCase().includes(q) ||
      item.description.toLowerCase().includes(q)
  );
};

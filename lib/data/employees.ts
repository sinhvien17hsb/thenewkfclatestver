export const getEmployeesByBranch = (branchId: string) =>
  employees.filter((e) => e.branchId === branchId);

export const getEmployeesByShift = (branchId: string, shift: string) =>
  employees.filter((e) => e.branchId === branchId && e.shift === shift);

export const getTopEmployees = (branchId: string, limit = 5) =>
  employees
    .filter((e) => e.branchId === branchId)
    .sort((a, b) => b.performanceScore - a.performanceScore)
    .slice(0, limit);

export const getEmployeeById = (id: string) =>
  employees.find((e) => e.id === id);

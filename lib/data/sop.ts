export const getBranchSOPCompliance = (branchId: string): number => {
  const branchCompletions = sopCompletions.filter((c) => c.branchId === branchId);
  if (branchCompletions.length === 0) return 0;
  const avg = branchCompletions.reduce((sum, c) => sum + c.complianceRate, 0) / branchCompletions.length;
  return Math.round(avg);
};
